#!/usr/bin/env node
/**
 * Generate embedded, bundler-safe TypeScript modules from on-disk assets:
 *
 *   src/generated/schemas.ts  — every schemas/**.json inlined as a static object,
 *                               keyed by its path relative to schemas/ (POSIX).
 *   src/generated/version.ts  — the package version as a static constant.
 *
 * Why: the runtime previously loaded schemas (and the version) via
 * `createRequire(import.meta.url)` + a path computed relative to the source file.
 * That dynamic, disk-relative load cannot be bundled into a single executable
 * (esbuild/SEA/pkg) and breaks if the file is relocated. Inlining them as static
 * imports makes ZAM bundler-safe and removes all runtime schema disk I/O.
 *
 * This script is deterministic and runs in `prebuild` and `pretest`, so the
 * generated files are always present and fresh (they are gitignored).
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { join, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const schemasDir = join(root, 'schemas');
const outDir = join(root, 'src', 'generated');

/** Recursively collect all .json file paths under a directory. */
function collectJson(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) out.push(...collectJson(full));
    else if (entry.endsWith('.json')) out.push(full);
  }
  return out;
}

const files = collectJson(schemasDir).sort();
if (files.length === 0) {
  console.error(`gen-embedded-schemas: no schemas found under ${schemasDir}`);
  process.exit(1);
}

const entries = files.map((full) => {
  const relPath = relative(schemasDir, full).split(sep).join('/'); // POSIX key
  const parsed = JSON.parse(readFileSync(full, 'utf8')); // validates JSON
  return [relPath, parsed];
});

mkdirSync(outDir, { recursive: true });

const header =
  '// AUTO-GENERATED by scripts/gen-embedded-schemas.mjs — DO NOT EDIT.\n' +
  '// Regenerate with `npm run gen:schemas`. Source of truth: the schemas/ directory.\n' +
  '/* eslint-disable */\n';

const schemaBody = entries
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
  .join('\n');

const schemasTs =
  header +
  '\n/** Every schemas/**.json inlined, keyed by path relative to schemas/ (POSIX). */\n' +
  'export const EMBEDDED_SCHEMAS: Record<string, Record<string, unknown>> = {\n' +
  schemaBody +
  '\n};\n';

writeFileSync(join(outDir, 'schemas.ts'), schemasTs);

const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const versionTs =
  header +
  '\n/** The package version, inlined so it survives single-binary bundling. */\n' +
  `export const PACKAGE_VERSION = ${JSON.stringify(pkg.version)};\n`;

writeFileSync(join(outDir, 'version.ts'), versionTs);

console.log(
  `gen-embedded-schemas: wrote ${entries.length} schemas + version ${pkg.version} to src/generated/`,
);
